public class AsciiValue {
    public static void main(String[] args) {
        // Ascii value is for Chatacters not for Strings
        System.out.println('a' + 'b'); // Print AsciiValue
        System.out.println("a" + "b"); // Print concatenation String
        System.out.println('a' + 3);
        System.out.println((char)('a' + 3)); // casting
        
    }
}
