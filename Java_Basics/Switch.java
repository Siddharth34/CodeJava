public class Switch {
    public static void main(String[] args) {
        int Id = 7;

        switch(Id){
            case 1:
                System.out.println("S");
                break;
            case 2:
                System.out.println("P");
                break;
            case 3:
                System.out.println("J");
                break;
            default:
                System.out.println("Enter Correct ID");
        }
    }
}
